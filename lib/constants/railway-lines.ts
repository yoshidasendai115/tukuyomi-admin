export type RailwayLine = keyof typeof RAILWAY_LINES;

// 路線一覧を取得
export const getRailwayLines = (): RailwayLine[] => {
  return Object.keys(RAILWAY_LINES) as RailwayLine[];
};

// 路線別に駅一覧を取得
export const getStationsByLine = (line: RailwayLine): readonly string[] => {
  return RAILWAY_LINES[line];
};

// 駅が所属する路線を取得
export const getRailwayLinesByStation = (stationName: string): RailwayLine[] => {
  const lines: RailwayLine[] = [];
  for (const [line, stations] of Object.entries(RAILWAY_LINES)) {
    if ((stations as readonly string[]).includes(stationName)) {
      lines.push(line as RailwayLine);
    }
  }
  return lines;
};

// 路線内での駅の順序を取得
export const getStationOrder = (line: RailwayLine, stationName: string): number => {
  const stations = RAILWAY_LINES[line] as readonly string[];
  return (stations as string[]).indexOf(stationName) + 1; // 1-based index
};